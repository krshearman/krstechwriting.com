                                </div>
                                <main role="main"><article role="article" aria-labelledby="ariaid-title1">
                                        <article class="topic concept glossentry nested1" aria-labelledby="ariaid-title2" id="LogicMonitor">
                                            <h3 class="title glossterm topictitle2 lead" id="ariaid-title2">LogicMonitor (LM)</h3>
                                            <div class="abstract glossdef">The comprehensive SaaS-based monitoring platform that provides end-to-end visibility into IT infrastructures.</div>
                                        </article><br>

                                        <article class="topic concept glossentry nested1" aria-labelledby="ariaid-title3" id="Collector">
                                            <h3 class="title glossterm topictitle2 lead" id="ariaid-title3">Collector</h3>
                                            <div class="abstract glossdef">A software component installed on a server to collect and send performance data to LogicMonitor.</div>
                                        </article><br>

                                        <article class="topic concept glossentry nested1" aria-labelledby="ariaid-title4" id="DataSource">
                                            <h3 class="title glossterm topictitle2 lead" id="ariaid-title4">DataSource</h3>
                                            <div class="abstract glossdef">A set of instructions defining how LogicMonitor collects, processes, and presents data from a monitored device or service.</div>
                                        </article><br>

                                        <article class="topic concept glossentry nested1" aria-labelledby="ariaid-title5" id="Dashboard">
                                            <h3 class="title glossterm topictitle2 lead" id="ariaid-title5">Dashboard</h3>
                                            <div class="abstract glossdef">A customizable interface that displays visual representations of monitored data for quick insights and analysis.</div>
                                        </article><br>

                                        <article class="topic concept glossentry nested1" aria-labelledby="ariaid-title6" id="Widget">
                                            <h3 class="title glossterm topictitle2 lead" id="ariaid-title6">Widget</h3>
                                            <div class="abstract glossdef">A visual component on a LogicMonitor dashboard that represents specific performance metrics or data.</div>
                                        </article><br>

                                        <article class="topic concept glossentry nested1" aria-labelledby="ariaid-title7" id="Alert">
                                            <h3 class="title glossterm topictitle2 lead" id="ariaid-title7">Alert</h3>
                                            <div class="abstract glossdef">A notification triggered by LogicMonitor when predefined thresholds or conditions are met, indicating potential issues.</div>
                                        </article><br>

                                        <article class="topic concept glossentry nested1" aria-labelledby="ariaid-title8" id="EscalationChain">
                                            <h3 class="title glossterm topictitle2 lead" id="ariaid-title8">Escalation Chain</h3>
                                            <div class="abstract glossdef">A sequence of steps and notifications LogicMonitor follows when alerting to ensure timely responses to issues.</div>
                                        </article><br>

                                        <article class="topic concept glossentry nested1" aria-labelledby="ariaid-title9" id="EscalationLevel">
                                            <h3 class="title glossterm topictitle2 lead" id="ariaid-title9">Escalation Level</h3>
                                            <div class="abstract glossdef">A stage within an escalation chain that defines the actions and notifications taken when an alert persists.</div>
                                        </article><br>

                                        <article class="topic concept glossentry nested1" aria-labelledby="ariaid-title10" id="DeviceGroup">
                                            <h3 class="title glossterm topictitle2 lead" id="ariaid-title10">Device Group</h3>
                                            <div class="abstract glossdef">A logical grouping of devices within LogicMonitor for streamlined management and monitoring.</div>
                                        </article><br>

                                        <article class="topic concept glossentry nested1" aria-labelledby="ariaid-title11" id="Instance">
                                            <h3 class="title glossterm topictitle2 lead" id="ariaid-title11">Instance</h3>
                                            <div class="abstract glossdef">A unique occurrence of a monitored item, such as a specific process or service on a device.</div>
                                        </article><br>

                                        <article class="topic concept glossentry nested1" aria-labelledby="ariaid-title12" id="Resource">
                                            <h3 class="title glossterm topictitle2 lead" id="ariaid-title12">Resource</h3>
                                            <div class="abstract glossdef">A component or feature of a monitored device, such as CPU, memory, or disk space.</div>
                                        </article><br>

                                        <article class="topic concept glossentry nested1" aria-labelledby="ariaid-title13" id="TopologyMap">
                                            <h3 class="title glossterm topictitle2 lead" id="ariaid-title13">Topology Map</h3>
                                            <div class="abstract glossdef">A visual representation of the relationships between devices in a network, helping understand dependencies and connections.</div>
                                        </article><br>

                                        <article class="topic concept glossentry nested1" aria-labelledby="ariaid-title14" id="SDT">
                                            <h3 class="title glossterm topictitle2 lead" id="ariaid-title14">SDT (Scheduled Downtime)</h3>
                                            <div class="abstract glossdef">A predefined time during which LogicMonitor suppresses alerts to accommodate planned maintenance.</div>
                                        </article><br>

                                        <article class="topic concept glossentry nested1" aria-labelledby="ariaid-title15" id="API">
                                            <h3 class="title glossterm topictitle2 lead" id="ariaid-title15">API (Application Programming Interface)</h3>
                                            <div class="abstract glossdef">A set of tools and protocols allowing external applications to interact with and retrieve data from LogicMonitor.</div>
                                        </article><br>

                                        <article class="topic concept glossentry nested1" aria-labelledby="ariaid-title16" id="AuditLogs">
                                            <h3 class="title glossterm topictitle2 lead" id="ariaid-title16">Audit Logs</h3>
                                            <div class="abstract glossdef">Records of all activities and changes within the LogicMonitor platform, providing a historical overview for troubleshooting and compliance.</div>
                                        </article><br>

                                        <article class="topic concept glossentry nested1" aria-labelledby="ariaid-title17" id="CollectorGroup">
                                            <h3 class="title glossterm topictitle2 lead" id="ariaid-title17">Collector Group</h3>
                                            <div class="abstract glossdef">A logical grouping of collectors for distributing the monitoring load and ensuring redundancy.</div>
                                        </article><br>

                                        <article class="topic concept glossentry nested1" aria-labelledby="ariaid-title18" id="CustomProperty">
                                            <h3 class="title glossterm topictitle2 lead" id="ariaid-title18">Custom Property</h3>
                                            <div class="abstract glossdef">A user-defined attribute assigned to devices or groups for additional categorization and organization.</div>
                                        </article><br>

                                        <article class="topic concept glossentry nested1" aria-labelledby="ariaid-title19" id="NetFlow">
                                            <h3 class="title glossterm topictitle2 lead" id="ariaid-title19">NetFlow</h3>
                                            <div class="abstract glossdef">A network protocol used for collecting and monitoring IP traffic information, providing insights into network behavior.</div>
                                        </article><br>

                                        <article class="topic concept glossentry nested1" aria-labelledby="ariaid-title20" id="LMConfigSource">
                                            <h3 class="title glossterm topictitle2 lead" id="ariaid-title20">LM ConfigSource</h3>
                                            <div class="abstract glossdef">LogicMonitor's configuration monitoring feature that tracks changes to configuration files on devices.</div>
                                        </article><br>

                                        <article class="topic concept glossentry nested1" aria-labelledby="ariaid-title21" id="LMCloud">
                                            <h3 class="title glossterm topictitle2 lead" id="ariaid-title21">LM Cloud</h3>
                                            <div class="abstract glossdef">A LogicMonitor feature that provides monitoring and insights into cloud-based infrastructures, supporting platforms like AWS, Azure, and Google Cloud.</div>
                                        </article><br>
