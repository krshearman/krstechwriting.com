                                        Glossary - Common Dita Terms                                    </h3>
                                </div>
                                <main role="main" class="lead"><article role="article" aria-labelledby="ariaid-title1">
                                        <article class="topic concept glossentry nested1" aria-labelledby="ariaid-title2" id="acronym">
                                            <h3 class="title glossterm topictitle2 head"id="ariaid-title2">Acronym:</h3>
                                            <div class="abstract glossdef">An abbreviation formed from the initial letters of a phrase or a series of words, often used in DITA content for concise representation.</div><br>
                                        </article>

                                        <article class="topic concept glossentry nested1" aria-labelledby="ariaid-title3" id="attribute">
                                            <h3 class="title glossterm topictitle2 head"id="ariaid-title3">Attribute:</h3>
                                            <div class="abstract glossdef">A property or characteristic associated with an XML element, providing additional information about the element.</div><br>
                                        </article>

                                        <article class="topic concept glossentry nested1" aria-labelledby="ariaid-title4" id="authoring-tool">
                                            <h3 class="title glossterm topictitle2 head"id="ariaid-title4">Authoring Tool:</h3>
                                            <div class="abstract glossdef">Software used for creating, editing, and managing DITA content, providing a user-friendly interface for authors.</div><br>
                                        </article>

                                        <article class="topic concept glossentry nested1" aria-labelledby="ariaid-title5" id="block-element">
                                            <h3 class="title glossterm topictitle2 head"id="ariaid-title5">Block Element:</h3>
                                            <div class="abstract glossdef">An XML element that can contain other elements, often used to define larger structural components in DITA.</div><br>
                                        </article>

                                        <article class="topic concept glossentry nested1" aria-labelledby="ariaid-title6" id="bookmap">
                                            <h3 class="title glossterm topictitle2 head"id="ariaid-title6">Bookmap:</h3>
                                            <div class="abstract glossdef">A DITA map specialization designed for authoring and organizing book-like content, such as manuals or guides.</div><br>
                                        </article>

                                        <article class="topic concept glossentry nested1" aria-labelledby="ariaid-title7" id="conref">
                                            <h3 class="title glossterm topictitle2 head"id="ariaid-title7">Conref (Content Reference):</h3>
                                            <div class="abstract glossdef">A mechanism in DITA that allows content to be reused by referencing it from one topic to another.</div><br>
                                        </article>

                                        <article class="topic concept glossentry nested1" aria-labelledby="ariaid-title8" id="dita">
                                            <h3 class="title glossterm topictitle2 head"id="ariaid-title8">DITA (Darwin Information Typing Architecture):</h3>
                                            <div class="abstract glossdef">An XML-based standard for creating and organizing modular and reusable content for technical documentation.</div><br>
                                        </article>

                                        <article class="topic concept glossentry nested1" aria-labelledby="ariaid-title9" id="dita-map">
                                            <h3 class="title glossterm topictitle2 head"id="ariaid-title9">DITA Map:</h3>
                                            <div class="abstract glossdef">A file that organizes and links together DITA topics, defining the structure and hierarchy of documentation.</div><br>
                                        </article>

                                        <article class="topic concept glossentry nested1" aria-labelledby="ariaid-title10" id="domain">
                                            <h3 class="title glossterm topictitle2 head"id="ariaid-title10">Domain:</h3>
                                            <div class="abstract glossdef">A specific area of knowledge or subject matter represented in DITA, often indicated by a specialized DTD or schema.</div><br>
                                        </article>

                                        <article class="topic concept glossentry nested1" aria-labelledby="ariaid-title11" id="element">
                                            <h3 class="title glossterm topictitle2 head"id="ariaid-title11">Element:</h3>
                                            <div class="abstract glossdef">A fundamental building block in XML and DITA, representing a component of structured content.</div><br>
                                        </article>

                                        <article class="topic concept glossentry nested1" aria-labelledby="ariaid-title12" id="filter">
                                            <h3 class="title glossterm topictitle2 head"id="ariaid-title12">Filter:</h3>
                                            <div class="abstract glossdef">A mechanism to selectively include or exclude content during the publishing process based on specified criteria.</div><br>
                                        </article>

                                        <article class="topic concept glossentry nested1" aria-labelledby="ariaid-title13" id="html5">
                                            <h3 class="title glossterm topictitle2 head"id="ariaid-title13">HTML5:</h3>
                                            <div class="abstract glossdef">A markup language used for structuring and presenting content on the web, often integrated with DITA publishing.</div><br>
                                        </article>

                                        <article class="topic concept glossentry nested1" aria-labelledby="ariaid-title14" id="keyref">
                                            <h3 class="title glossterm topictitle2 head"id="ariaid-title14">Keyref:</h3>
                                            <div class="abstract glossdef">A DITA feature that enables the reuse of key-based content references, facilitating consistent information across topics.</div><br>
                                        </article>

                                        <article class="topic concept glossentry nested1" aria-labelledby="ariaid-title15" id="learning-specialization">
                                            <h3 class="title glossterm topictitle2 head"id="ariaid-title15">Learning and Training Specialization:</h3>
                                            <div class="abstract glossdef">A DITA specialization tailored for creating instructional and training materials.</div><br>
                                        </article>

                                        <article class="topic concept glossentry nested1" aria-labelledby="ariaid-title16" id="linking">
                                            <h3 class="title glossterm topictitle2 head"id="ariaid-title16">Linking:</h3>
                                            <div class="abstract glossdef">The process of creating hyperlinks between DITA topics or other resources to establish connections and references.</div><br>
                                        </article>

                                        <article class="topic concept glossentry nested1" aria-labelledby="ariaid-title17" id="metadata">
                                            <h3 class="title glossterm topictitle2 head"id="ariaid-title17">Metadata:</h3>
                                            <div class="abstract glossdef">Information about content, such as title, author, or keywords, providing context and facilitating organization.</div><br>
                                        </article>

                                        <article class="topic concept glossentry nested1" aria-labelledby="ariaid-title18" id="nested-element">
                                            <h3 class="title glossterm topictitle2 head"id="ariaid-title18">Nested Element:</h3>
                                            <div class="abstract glossdef">An XML element contained within another element, forming a hierarchical structure in DITA content.</div><br>
                                        </article>

                                        <article class="topic concept glossentry nested1" aria-labelledby="ariaid-title19" id="output-format">
                                            <h3 class="title glossterm topictitle2 head"id="ariaid-title19">Output Format:</h3>
                                            <div class="abstract glossdef">The desired presentation or delivery format for DITA content, such as PDF, HTML, or other publishing outputs.</div><br>
                                        </article>

                                        <article class="topic concept glossentry nested1" aria-labelledby="ariaid-title20" id="pdf2">
                                            <h3 class="title glossterm topictitle2 head"id="ariaid-title20">PDF2:</h3>
                                            <div class="abstract glossdef">A DITA-OT transformation type for generating PDF output from DITA content.</div><br>
                                        </article>

                                        <article class="topic concept glossentry nested1" aria-labelledby="ariaid-title21" id="profile">
                                            <h3 class="title glossterm topictitle2 head"id="ariaid-title21">Profile:</h3>
                                            <div class="abstract glossdef">A predefined set of rules or configurations that can be applied to DITA content during processing.</div><br>
                                        </article>

                                        <article class="topic concept glossentry nested1" aria-labelledby="ariaid-title22" id="specialization">
                                            <h3 class="title glossterm topictitle2 head"id="ariaid-title22">Specialization:</h3>
                                            <div class="abstract glossdef">The process of extending or customizing DITA to create domain-specific structures and rules.</div><br>
                                        </article>

                                        <article class="topic concept glossentry nested1" aria-labelledby="ariaid-title23" id="stub">
                                            <h3 class="title glossterm topictitle2 head"id="ariaid-title23">Stub:</h3>
                                            <div class="abstract glossdef">A placeholder topic in a DITA map, often used to reserve a location for future content.</div><br>
                                        </article>

                                        <article class="topic concept glossentry nested1" aria-labelledby="ariaid-title24" id="subject-scheme">
                                            <h3 class="title glossterm topictitle2 head"id="ariaid-title24">Subject Scheme:</h3>
                                            <div class="abstract glossdef">A controlled vocabulary or taxonomy used to categorize and organize DITA content.</div><br>
                                        </article>

                                        <article class="topic concept glossentry nested1" aria-labelledby="ariaid-title25" id="task-specialization">
                                            <h3 class="title glossterm topictitle2 head"id="ariaid-title25">Task Specialization:</h3>
                                            <div class="abstract glossdef">A DITA specialization designed for creating procedural and instructional content, such as task-oriented topics.</div><br>
                                        </article>

                                        <article class="topic concept glossentry nested1" aria-labelledby="ariaid-title26" id="topic">
                                            <h3 class="title glossterm topictitle2 head"id="ariaid-title26">Topic:</h3>
                                            <div class="abstract glossdef">The fundamental unit of content in DITA, representing a standalone piece of information.</div><br>
                                        </article>

                                        <article class="topic concept glossentry nested1" aria-labelledby="ariaid-title27" id="transclusion">
                                            <h3 class="title glossterm topictitle2 head"id="ariaid-title27">Transclusion:</h3>
                                            <div class="abstract glossdef">The inclusion of content from one DITA topic into another using the xref element.</div><br>
                                        </article>

                                        <article class="topic concept glossentry nested1" aria-labelledby="ariaid-title28" id="uri">
                                            <h3 class="title glossterm topictitle2 head"id="ariaid-title28">Uniform Resource Identifier (URI):</h3>
                                            <div class="abstract glossdef">A string of characters used to identify and locate resources, such as DITA topics or images.</div><br>
                                        </article>

                                        <article class="topic concept glossentry nested1" aria-labelledby="ariaid-title29" id="variable">
                                            <h3 class="title glossterm topictitle2 head"id="ariaid-title29">Variable:</h3>
                                            <div class="abstract glossdef">A placeholder for values that can be dynamically replaced during the publishing process.</div><br>
                                        </article>

                                        <article class="topic concept glossentry nested1" aria-labelledby="ariaid-title30" id="versioning">
                                            <h3 class="title glossterm topictitle2 head"id="ariaid-title30">Versioning:</h3>
                                            <div class="abstract glossdef">The management and tracking of different versions of DITA content, often used in collaborative authoring environments.</div><br>
                                        </article>

                                        <article class="topic concept glossentry nested1" aria-labelledby="ariaid-title31" id="wysiwyg">
                                            <h3 class="title glossterm topictitle2 head"id="ariaid-title31">WYSIWYG (What You See Is What You Get):</h3>
                                            <div class="abstract glossdef">An authoring mode in which the content appears as it will be presented in the final output, simplifying the editing process.</div><br>
                                        </article>

                                        <article class="topic concept glossentry nested1" aria-labelledby="ariaid-title32" id="xhtml">
                                            <h3 class="title glossterm topictitle2 head"id="ariaid-title32">XHTML:</h3>
                                            <div class="abstract glossdef">A markup language that extends HTML, providing compatibility with XML and used in DITA content for web-based outputs.</div><br>
                                        </article>

